# -*- coding: utf-8 -*-
"""
╔══════════════════════════════════════════════════════════════════╗
║  Лабораторна робота №3. Варіант 11.                             ║
║  Аналітичне дослідження розімкнутої мережі масового             ║
║  обслуговування (ММО)                                           ║
╚══════════════════════════════════════════════════════════════════╝

Структура мережі (по схемі):

  λ₀ ──→ [Q₁] ──→ СМО1(K₁) ──→ (1-P₁₁)=0.4 ──────────────────────┐
              ↑                                                       │
              └──────────────── P₁₁=0.6 ──────────────────────────── ┘
                                                                      │
                     ┌── P₂₂=0.25 ←──────────────────────────────┐  │
                     ↓                                             │  │
                   [Q₂] ──→ K₂ (пар. 1) ──┐                      │  │
                        ──→ K₂ (пар. 2) ──┼──→ (1-P₂₂)=0.75 ────┘  │
                        ──→ K₂ (пар. 3) ──┘          │               │
                                                       ↓               │
                                                 [Q₃] ──→ СМО3 ──→ Вихід
                                                                      ↑
                          P₂₂ охоплює увесь прямокутний блок ────────┘

Вхідні дані (N=100):
  СМО1 (K₁) : 100 дет / 2 год  → μ₁ = 50.0  дет/год
  K₂×3 пар. : 100 дет / 8 год  → μ  = 12.5  дет/год  (кожен)
  СМО3       : 100 дет / 8 год  → μ₅ = 12.5  дет/год
  P₁₁ = 0.6, P₂₂ = 0.25, λ₀ = 10 дет/год
"""

import numpy as np

# ─────────────────────────────────────────────────────────────────
# 0. НАЛАШТУВАННЯ ВИВОДУ
# ─────────────────────────────────────────────────────────────────
SEP  = "=" * 68
sep  = "-" * 68

def section(title):
    print(f"\n{'─'*68}")
    print(f"  {title}")
    print(f"{'─'*68}")

# ─────────────────────────────────────────────────────────────────
# 1. ВХІДНІ ДАНІ
# ─────────────────────────────────────────────────────────────────
print(SEP)
print("  ЛАБОРАТОРНА РОБОТА №3  |  Варіант 11")
print("  Аналітичне дослідження розімкнутої ММО")
print(SEP)

lambda0 = 10.0    # зовнішній вхідний потік, дет/год
P11     = 0.60    # зворотній зв'язок СМО1 → вхід СМО1
P22     = 0.25    # зворотній зв'язок паралельного блоку → його вхід

#  Вузол        : назва          μ (дет/год)   r (каналів)
NODES = [
    ("СМО1",   "K₁",  100/2,  1),   # вузол 1
    ("пар.1",  "K₂",  100/8,  1),   # вузол 2
    ("пар.2",  "K₂",  100/8,  1),   # вузол 3
    ("пар.3",  "K₂",  100/8,  1),   # вузол 4
    ("СМО3",   "K₃",  100/8,  1),   # вузол 5
]
n      = len(NODES)
names  = [f"{nd[0]}({nd[1]})" for nd in NODES]
mu     = np.array([nd[2] for nd in NODES])
r      = np.array([nd[3] for nd in NODES])

section("ВХІДНІ ПАРАМЕТРИ")
print(f"  Зовнішній потік:       λ₀  = {lambda0} дет/год")
print(f"  Зворотній зв'язок 1:   P₁₁ = {P11}  (СМО1 → вхід СМО1)")
print(f"  Зворотній зв'язок 2:   P₂₂ = {P22}  (пар.блок → вхід пар.блоку)")
print()
print(f"  {'№':<4} {'Вузол':<14} {'Тип':<6} {'μᵢ (дет/год)':>14} {'rᵢ':>5}")
print(f"  {'─'*46}")
for i, nd in enumerate(NODES):
    print(f"  {i+1:<4} {nd[0]:<14} {nd[1]:<6} {nd[2]:>14.4f} {nd[3]:>5}")

# ─────────────────────────────────────────────────────────────────
# 2. КРОК 1 — КОЕФІЦІЄНТИ ПЕРЕДАЧІ e_i
# ─────────────────────────────────────────────────────────────────
section("КРОК 1.  Коефіцієнти передачі eᵢ  (рівняння балансу вузлів)")

# ┌─ Виведення рівнянь ─────────────────────────────────────────┐
# │ Вузол СМО1:                                                  │
# │   λ₁ = λ₀ + P₁₁·λ₁                                         │
# │   λ₁(1−P₁₁) = λ₀   →   e₁ = 1/(1−P₁₁)                    │
# │                                                               │
# │ Вузол перед паралельним блоком:                              │
# │   Σλ_пар = (1−P₁₁)·λ₁ + P₂₂·Σλ_пар                        │
# │   λ₀ + P₂₂·Σλ_пар = Σλ_пар                                 │
# │   Σλ_пар·(1−P₂₂) = λ₀                                      │
# │   Σe_пар = 1/(1−P₂₂)                                        │
# │   Симетрія → e₂ = e₃ = e₄ = Σe_пар/3                       │
# │                                                               │
# │ Вузол СМО3:                                                  │
# │   λ₅ = (1−P₂₂)·Σλ_пар = λ₀   →   e₅ = 1                  │
# └─────────────────────────────────────────────────────────────┘

e = np.zeros(n)
e[0]        = 1.0 / (1.0 - P11)       # = 2.5
e_par       = 1.0 / (1.0 - P22)       # = 4/3 ≈ 1.3333
e[1]=e[2]=e[3] = e_par / 3.0          # = 4/9 ≈ 0.4444
e[4]        = 1.0

lambda_i = e * lambda0

print(f"  e₁ = 1/(1−P₁₁) = 1/(1−{P11}) = {e[0]:.6f}")
print(f"  Σe_пар = e₂+e₃+e₄ = 1/(1−P₂₂) = 1/(1−{P22}) = {e_par:.6f}")
print(f"  e₂ = e₃ = e₄ = {e_par:.6f} / 3 = {e[1]:.6f}")
print(f"  e₅ = 1.0  (весь потік виходить через СМО3)")
print()
print(f"  {'№':<4} {'Вузол':<14} {'eᵢ':>10} {'λᵢ = eᵢ·λ₀ (дет/год)':>22}")
print(f"  {'─'*52}")
for i in range(n):
    print(f"  {i+1:<4} {names[i]:<14} {e[i]:>10.6f} {lambda_i[i]:>22.6f}")

# ─────────────────────────────────────────────────────────────────
# 3. КРОК 2 — УМОВА СТАЦІОНАРНОСТІ
# ─────────────────────────────────────────────────────────────────
section("КРОК 2.  Перевірка умови стаціонарності  λᵢ < μᵢ · rᵢ")

steady_ok = True
for i in range(n):
    cap = mu[i] * r[i]
    ok  = bool(lambda_i[i] < cap)
    steady_ok = steady_ok and ok
    mark = "✓" if ok else "✗"
    print(f"  {mark} {names[i]:<14}: {lambda_i[i]:>7.4f} < {cap:>7.4f}  →  "
          f"{'виконується' if ok else 'НЕ виконується!'}")

print()
if steady_ok:
    print("  ✓ Система знаходиться у стаціонарному режимі.")
else:
    print("  ✗ УВАГА: є перевантажені вузли — стаціонарний режим неможливий!")

# ─────────────────────────────────────────────────────────────────
# 4. КРОК 3 — КОЕФІЦІЄНТИ ЗАВАНТАЖЕННЯ
# ─────────────────────────────────────────────────────────────────
section("КРОК 3.  Коефіцієнти завантаження  ρᵢ = λᵢ / μᵢ")

rho = lambda_i / mu

print(f"  {'№':<4} {'Вузол':<14} {'λᵢ':>8} {'μᵢ':>8} {'ρᵢ':>10} {'%':>7}")
print(f"  {'─'*54}")
for i in range(n):
    bar = "█" * int(rho[i] * 25)
    print(f"  {i+1:<4} {names[i]:<14} {lambda_i[i]:>8.4f} {mu[i]:>8.4f} "
          f"{rho[i]:>10.6f} {rho[i]*100:>6.2f}%  {bar}")

# ─────────────────────────────────────────────────────────────────
# 5. КРОК 4 — НОРМУЮЧІ МНОЖНИКИ
# ─────────────────────────────────────────────────────────────────
section("КРОК 4.  Нормуючі множники  Cᵢ = 1/(1−ρᵢ)  [M/M/1]")

C = np.array([1.0/(1.0-rho[i]) if rho[i]<1 else np.nan for i in range(n)])

print(f"  {'№':<4} {'Вузол':<14} {'ρᵢ':>10} {'Cᵢ = 1/(1−ρᵢ)':>16}")
print(f"  {'─'*46}")
for i in range(n):
    print(f"  {i+1:<4} {names[i]:<14} {rho[i]:>10.6f} {C[i]:>16.6f}")

# ─────────────────────────────────────────────────────────────────
# 6. КРОК 5 — ПОКАЗНИКИ ЕФЕКТИВНОСТІ КОЖНОЇ СМО
# ─────────────────────────────────────────────────────────────────
section("КРОК 5.  Показники ефективності кожної СМО  (модель M/M/1)")

print("  Формули для одноканальної СМО без обмеження черги (M/M/1):")
print("    Rᵢ = ρᵢ                     — сер. число зайнятих каналів")
print("    Lᵢ = ρᵢ² / (1−ρᵢ)          — сер. довжина черги (очікуючі)")
print("    Mᵢ = ρᵢ / (1−ρᵢ) = Rᵢ+Lᵢ  — сер. число вимог у системі")
print("    Tᵢ = Mᵢ / λᵢ               — сер. час у системі (Літтл)")
print("    Qᵢ = Lᵢ / λᵢ               — сер. час очікування (Літтл)")
print()

R_i = rho.copy()
L_i = rho**2 / (1.0 - rho)
M_i = rho    / (1.0 - rho)
T_i = M_i / lambda_i
Q_i = L_i / lambda_i

print(f"  {'№':<4} {'Вузол':<14} {'Rᵢ':>7} {'Lᵢ':>7} {'Mᵢ':>7} "
      f"{'Tᵢ(год)':>9} {'Tᵢ(хв)':>8} {'Qᵢ(год)':>9} {'Qᵢ(хв)':>8}")
print(f"  {'─'*78}")
for i in range(n):
    print(f"  {i+1:<4} {names[i]:<14} {R_i[i]:>7.4f} {L_i[i]:>7.4f} {M_i[i]:>7.4f} "
          f"{T_i[i]:>9.4f} {T_i[i]*60:>8.4f} {Q_i[i]:>9.4f} {Q_i[i]*60:>8.4f}")

# ─────────────────────────────────────────────────────────────────
# 7. КРОК 6 — ПОКАЗНИКИ ВСІЄЇ МЕРЕЖІ
# ─────────────────────────────────────────────────────────────────
section("КРОК 6.  Показники ефективності всієї мережі")

T_net = float(np.dot(e, T_i))   # Σ eᵢ·Tᵢ
Q_net = float(np.dot(e, Q_i))   # Σ eᵢ·Qᵢ
M_net = lambda0 * T_net          # Формула Літтла для мережі
L_net = float(np.sum(L_i))      # Σ Lᵢ

print(f"  Середній час перебування деталі в ММО:")
print(f"    T = Σ eᵢ·Tᵢ = {e[0]}·{T_i[0]:.4f} + 3·{e[1]:.4f}·{T_i[1]:.4f} + {e[4]}·{T_i[4]:.4f}")
print(f"    T = {e[0]*T_i[0]:.4f} + {3*e[1]*T_i[1]:.4f} + {e[4]*T_i[4]:.4f}")
print(f"    T = {T_net:.6f} год  =  {T_net*60:.4f} хв")
print()
print(f"  Середній час очікування в чергах:")
print(f"    Q = Σ eᵢ·Qᵢ = {Q_net:.6f} год  =  {Q_net*60:.4f} хв")
print()
print(f"  Середнє число деталей у мережі (ф-ла Літтла: M = λ₀·T):")
print(f"    M = {lambda0} · {T_net:.6f} = {M_net:.6f} дет")
print()
print(f"  Сумарна середня довжина черг:")
print(f"    L = Σ Lᵢ = {' + '.join([f'{L_i[i]:.4f}' for i in range(n)])}")
print(f"    L = {L_net:.6f} дет")

# ─────────────────────────────────────────────────────────────────
# 8. КРОК 7 — ЗВЕДЕНА ТАБЛИЦЯ
# ─────────────────────────────────────────────────────────────────
section("КРОК 7.  Зведена таблиця результатів")

print(f"  {'Параметр':<38} {'СМО1(K₁)':>10} {'кожен K₂':>10} {'СМО3(K₃)':>10}")
print(f"  {'─'*70}")
rows = [
    ("eᵢ  — коефіцієнт передачі",        e[0],        e[1],        e[4]),
    ("λᵢ  — інтенсивність, дет/год",      lambda_i[0], lambda_i[1], lambda_i[4]),
    ("μᵢ  — продуктивність, дет/год",     mu[0],       mu[1],       mu[4]),
    ("ρᵢ  — завантаженість",              rho[0],      rho[1],      rho[4]),
    ("Rᵢ  — сер. зайнятих каналів",       R_i[0],      R_i[1],      R_i[4]),
    ("Lᵢ  — сер. довжина черги, дет",     L_i[0],      L_i[1],      L_i[4]),
    ("Mᵢ  — сер. число у системі, дет",   M_i[0],      M_i[1],      M_i[4]),
    ("Tᵢ  — сер. час у системі, год",     T_i[0],      T_i[1],      T_i[4]),
    ("Qᵢ  — сер. час очікування, год",    Q_i[0],      Q_i[1],      Q_i[4]),
]
for name, v1, v2, v5 in rows:
    print(f"  {name:<38} {v1:>10.4f} {v2:>10.4f} {v5:>10.4f}")

print(f"\n  * «кожен K₂» — показники одного з трьох паралельних вузлів")
print(f"    (усі три ідентичні за симетрією)")

print(f"\n  Показники мережі в цілому:")
print(f"  {'─'*44}")
print(f"  {'T — сер. час перебування, год':<38} {T_net:>10.4f}")
print(f"  {'T — сер. час перебування, хв':<38} {T_net*60:>10.4f}")
print(f"  {'Q — сер. час очікування, год':<38} {Q_net:>10.4f}")
print(f"  {'Q — сер. час очікування, хв':<38} {Q_net*60:>10.4f}")
print(f"  {'M — сер. число деталей у мережі':<38} {M_net:>10.4f}")
print(f"  {'L — сумарна сер. черга, дет':<38} {L_net:>10.4f}")

# ─────────────────────────────────────────────────────────────────
# 9. ВИСНОВКИ
# ─────────────────────────────────────────────────────────────────
section("ВИСНОВКИ")

idx_max = int(np.argmax(rho))
pct_wait = Q_net / T_net * 100

print(f"  1. СТАЦІОНАРНІСТЬ: Всі коефіцієнти завантаження ρᵢ < 1,")
print(f"     система перебуває у стаціонарному режимі.")
print()
print(f"  2. ВУЗЬКЕ МІСЦЕ: {names[idx_max]} має найбільше завантаження")
print(f"     ρ = {rho[idx_max]*100:.2f}% та найдовшу чергу L = {L_i[idx_max]:.4f} дет.")
print(f"     Саме цей вузол обмежує пропускну здатність мережі.")
print()
print(f"  3. ВПЛИВ P₁₁={P11}: зворотній зв'язок СМО1 збільшує")
print(f"     кількість проходів через СМО1 в e₁={e[0]:.1f} рази,")
print(f"     підвищуючи λ₁ з {lambda0} до {lambda_i[0]:.1f} дет/год (ρ₁={rho[0]*100:.0f}%).")
print()
print(f"  4. ВПЛИВ P₂₂={P22}: зворотній зв'язок паралельного блоку")
print(f"     збільшує сумарний потік у блок у {e_par:.4f} рази")
print(f"     (Σλ_пар = {lambda_i[1]*3:.4f} дет/год замість λ₀={lambda0}).")
print()
print(f"  5. ПАРАЛЕЛЬНИЙ БЛОК: три канали K₂ рівномірно ділять")
print(f"     навантаження — λ кожного = {lambda_i[1]:.4f} дет/год, ρ = {rho[1]*100:.2f}%.")
print(f"     Паралелізм суттєво знижує завантаження порівняно")
print(f"     з одним каналом (якби 1×K₂: ρ = {lambda_i[1]*3/mu[1]*100:.2f}%).")
print()
print(f"  6. ЧАС У МЕРЕЖІ: деталь проводить у мережі T = {T_net*60:.2f} хв,")
print(f"     з яких Q = {Q_net*60:.2f} хв ({pct_wait:.1f}%) — очікування в чергах,")
print(f"     а {(T_net-Q_net)*60:.2f} хв ({100-pct_wait:.1f}%) — безпосередньо обслуговування.")
print()
print(f"  7. ЗАВАНТАЖЕНІСТЬ:")
for i in range(n):
    bar = "█" * int(rho[i]*30) + "░" * (30 - int(rho[i]*30))
    print(f"     {names[i]:<14}: {bar}  {rho[i]*100:5.2f}%")

print()
print(SEP)
print("  Кінець розрахунків. Лабораторна робота №3, Варіант 11.")
print(SEP)
